class Node:
  # constructor for defining the data and next
  def __init__(self, data = None, next=None): 
    self.data = data
    self.next = next

# A Linked List class with a single head node
class LinkedList:
  def __init__(self):  
    self.head = None
  
  # insertion method 
  def insert(self, data):
    newNode = Node(data)
